#El condicional if se ejecuta si la condicion es TRUE
#si es FALSE se salta el codigo dentro del if

#las condiciones que podemos escribir en los ifs son booleanas (true o false)
#operadores: <, >, <= menor o igual, >= mayor o igual, == igual que, != diferente

num1 = 15
num2 = 20

if num1 < num2: 
	print('Se ejecuta el if.')
        #la condicion es TRUE
	
num1 = 1450
num2 = 60

if num1 > num2:
	print('Se ejecuta el if.')
        #la condición es TRUE

num1 = 1450
num2 = 60

if num1 == num2:
	print('Se ejecuta el if.')
        #la condición es FALSE, no se imprime
