#las posiciones negativas en las listas se utilizan para
#acceder mas facilmente a los ultimos elementos de una lista

#las posiciones negativas comienzan a contar desde el ultimo elemento
#hasta el primero

colores = ['rojo', 'azul', 'verde', 'amarillo', 'marrón', 'lila', 'negro', 'rosa', 'blanco', 'naranja']

#la posición -1 sería la última posición, la -2 la penúltima y así sucesivamente
print(colores[-1])
print(colores[-7])
print(colores[-5])
print(colores[-2])
print(colores[-10])
